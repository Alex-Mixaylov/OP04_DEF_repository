
# Напишите функцию **sum_range(start, end)**,
# которая суммирует все ЦЕЛЫЕ числа от значения **start** до
# величины **end** включительно
def sum_range(start, end):
    # Проверяем, чтобы начальное значение было меньше или равно конечному
    if start > end:
        return "Начальное значение должно быть меньше или равно конечному значению"

    # Инициализируем переменную для суммы
    total = 0

    # Цикл for для перебора каждого числа в заданном диапазоне включая end (end + 1)
    # Используем range с округлением до целых чисел
    for number in range(int(start), int(end) + 1):
        total += number  # Суммируем число

    return total

# Пример вызова функции
# код будет правильно обрабатывать ввод значений с плавающей точкой,
# однако функция range работает только с целыми числами,
# поэтому вводимые значения будут округляться до ближайшего целого числа для суммирования.

start = float(input("Введите начальное значение: "))
end = float(input("Введите конечное значение: "))
result = sum_range(start, end)
print(f"Сумма чисел от {start} до {end} включительно = {result}")  # Сумма чисел

def sum_range(start, end):
    # Проверяем, чтобы начальное значение было меньше или равно конечному
    if start > end:
        return "Начальное значение должно быть меньше или равно конечному значению"

    # Инициализируем переменную для суммы
    total = 0

    # Цикл for для перебора каждого числа в заданном диапазоне включая end (end + 1)
    for number in range(start, end + 1):
        total += number  # Суммируем число

    return total

# Пример вызова функции
result = sum_range(start=3, end=7)
print(f"Сумма чисел от 3 до 7 включительно = {result}")  # Сумма чисел от 3 до 7
